// Package golang generates Go code from XPB schemas.
package golang

import (
	"bytes"
	"fmt"
	"go/format"
	"strings"

	"github.com/ElecTwix/xpb/pkg/ast"
	"github.com/ElecTwix/xpb/pkg/codegen/common"
	"github.com/ElecTwix/xpb/pkg/wire"
)

// Optional-field representation styles for Options.OptionalStyle.
const (
	// OptionalPointer represents optional scalar/string/bytes/enum fields as
	// *T, so nil distinguishes "absent" from a zero value. This is the
	// default and the backward-compatible behavior.
	OptionalPointer = "pointer"
	// OptionalValue represents optional fields as a value field plus a
	// generated Has<Field> bool. This avoids the per-present-field pointer-
	// boxing heap allocation that *T optionals incur on decode.
	OptionalValue = "value"
)

// Options controls Go code generation.
type Options struct {
	// OptionalStyle selects how optional scalar/string/bytes/enum fields are
	// represented: OptionalPointer (default) or OptionalValue. Non-enum
	// message optionals are always *T regardless of this setting, since the
	// pointer is the natural absence marker and decode allocates the nested
	// struct anyway.
	OptionalStyle string
	// ZeroCopyBytes makes bytes fields decode by aliasing the decoder's input
	// buffer (ReadBytesUnsafe) instead of copying it (ReadBytes). The decoded
	// []byte stays valid only while the source buffer is unmodified and alive.
	ZeroCopyBytes bool
}

// Generator generates Go code from an AST.
type Generator struct {
	buf   bytes.Buffer
	enums map[string]bool // Track enum names for resolution
	opts  Options
}

// Generate generates Go code for the given file AST using default options.
func Generate(file *ast.File) ([]byte, error) {
	return GenerateWithOptions(file, Options{})
}

// GenerateWithOptions generates Go code for the given file AST using the
// supplied options.
func GenerateWithOptions(file *ast.File, opts Options) ([]byte, error) {
	g := &Generator{
		enums: make(map[string]bool),
		opts:  opts,
	}
	return g.generate(file)
}

// valueOptional reports whether field should use the value+Has<Field>
// representation rather than a *T pointer. Only optional scalar/string/bytes/
// enum fields qualify; non-enum message optionals always stay pointers.
func (g *Generator) valueOptional(field *ast.Field) bool {
	if g.opts.OptionalStyle != OptionalValue || !field.Optional {
		return false
	}
	if field.Type.Kind == ast.TypeMessage && !g.isEnumType(field.Type) {
		return false
	}
	return true
}

// bytesReadCall returns the stateless cursor expression for reading a bytes
// field, honoring the ZeroCopyBytes option. The expression yields
// `(value, newPos, error)` and takes (data, pos) as the cursor.
func (g *Generator) bytesReadCall() string {
	if g.opts.ZeroCopyBytes {
		return "xpb.ReadBytesUnsafeAt(data, pos)"
	}
	return "xpb.ReadBytesAt(data, pos)"
}

func (g *Generator) generate(file *ast.File) ([]byte, error) {
	g.buf.Reset()

	// Build enum set for type resolution
	for _, enum := range file.Enums {
		g.enums[enum.Name] = true
	}

	pkg := file.Package
	if pkg == "" {
		pkg = "main"
	}
	g.printf("// Code generated by xpbc. DO NOT EDIT.\n")
	g.printf("package %s\n\n", pkg)

	g.printf("import (\n")
	g.printf("\t\"github.com/ElecTwix/xpb/runtime/go/xpb\"\n")
	g.printf(")\n\n")

	g.printf("var _ = xpb.NewEncoder\n\n")

	// Generate enums
	for _, enum := range file.Enums {
		g.generateEnum(enum)
	}

	// Generate messages
	for _, msg := range file.Messages {
		if err := g.generateMessage(msg); err != nil {
			return nil, err
		}
	}

	src, err := format.Source(g.buf.Bytes())
	if err != nil {
		return g.buf.Bytes(), fmt.Errorf("format error: %w", err)
	}
	return src, nil
}

func (g *Generator) printf(format string, args ...any) {
	fmt.Fprintf(&g.buf, format, args...)
}

func (g *Generator) generateEnum(enum *ast.Enum) {
	name := enum.Name
	g.printf("// %s is an enum type.\n", name)
	g.printf("type %s int32\n\n", name)
	g.printf("const (\n")
	for _, v := range enum.Values {
		g.printf("\t%s_%s %s = %d\n", name, v.Name, name, v.Number)
	}
	g.printf(")\n\n")

	g.printf("func (e %s) String() string {\n", name)
	g.printf("\tswitch e {\n")
	for _, v := range enum.Values {
		g.printf("\tcase %s_%s:\n", name, v.Name)
		g.printf("\t\treturn \"%s\"\n", v.Name)
	}
	g.printf("\tdefault:\n")
	g.printf("\t\treturn \"UNKNOWN\"\n")
	g.printf("\t}\n")
	g.printf("}\n\n")
}

func (g *Generator) generateMessage(msg *ast.Message) error {
	name := msg.Name

	// Struct
	g.printf("// %s is a generated XPB message.\n", name)
	g.printf("type %s struct {\n", name)
	for _, field := range msg.Fields {
		goType := g.goTypeName(field)
		g.printf("\t%s %s\n", toCamelCase(field.Name), goType)
		if g.valueOptional(field) {
			g.printf("\tHas%s bool\n", toCamelCase(field.Name))
		}
	}
	g.printf("}\n\n")

	// Marshal - V2: no tags, fields in order
	g.printf("func (m *%s) Marshal() ([]byte, error) {\n", name)
	g.printf("\tenc := xpb.NewEncoder(%d)\n", estimateSize(msg))
	for _, field := range msg.Fields {
		g.generateFieldEncode(field)
	}
	g.printf("\treturn enc.Bytes(), nil\n")
	g.printf("}\n\n")

	// MarshalTo
	g.printf("func (m *%s) MarshalTo(enc *xpb.Encoder) {\n", name)
	for _, field := range msg.Fields {
		g.generateFieldEncode(field)
	}
	g.printf("}\n\n")

	// Unmarshal - V2: no tags, read fields in order.
	// The depth-cap helper unmarshalAt is unexported on purpose: xpbc emits
	// one Go package per .xpb file and the schema grammar has no `import`
	// directive (see pkg/parser/parser.go), so every nested call is intra-
	// package. If cross-package message references are ever added, this
	// helper must be promoted to UnmarshalAt or the depth must be threaded
	// some other way.
	g.printf("func (m *%s) Unmarshal(data []byte) error {\n", name)
	g.printf("\treturn m.unmarshalAt(data, 0)\n")
	g.printf("}\n\n")

	g.printf("func (m *%s) unmarshalAt(data []byte, depth int) error {\n", name)
	g.printf("\tif depth > xpb.MaxDecodeDepth { return xpb.ErrMaxDepthExceeded }\n")
	if len(msg.Fields) == 0 {
		// A message with no fields decodes nothing; declaring a cursor would
		// leave `pos` (and `data`) unused, which Go rejects at compile time.
		g.printf("\t_ = data\n")
	} else {
		// Decode threads a register-local int cursor (`pos`) through the
		// stateless xpb.*At helpers instead of carrying it in a Decoder struct
		// field. Centralized bounds/compact-length/array-count logic stays in
		// the runtime helpers; the generated body just advances the cursor. The
		// function-scoped `pos`/`err` are reassigned (=) inside the per-field
		// blocks so the cursor is shared across fields rather than shadowed.
		g.printf("\tpos := 0\n")
		g.printf("\tvar err error\n")
		for _, field := range msg.Fields {
			g.generateFieldDecode(field)
		}
	}
	g.printf("\treturn nil\n")
	g.printf("}\n\n")

	return nil
}

// isEnumType checks if the type name refers to an enum
func (g *Generator) isEnumType(t ast.FieldType) bool {
	if t.Kind == ast.TypeMessage && g.enums[t.Message] {
		return true
	}
	return t.Kind == ast.TypeEnum
}

func (g *Generator) generateFieldEncode(field *ast.Field) {
	fieldName := toCamelCase(field.Name)
	isEnum := g.isEnumType(field.Type)

	// Optional fields carry a 1-byte presence flag (see docs/WIRE_FORMAT.md):
	// 0x01 + value when present, 0x00 (and nothing more) when absent. Optional
	// is only valid on scalar/string/bytes/enum/message fields — never on
	// repeated/map, which already carry counts — so the repeated/map branches
	// below are unreachable when field.Optional is set.
	if field.Optional {
		if field.Type.Kind == ast.TypeMessage && !isEnum {
			// Nested message optionals are *T already. Emit the presence
			// byte, then the message body only when non-nil.
			g.printf("\tenc.WriteBool(m.%s != nil)\n", fieldName)
			g.printf("\tif m.%s != nil {\n", fieldName)
			g.generateScalarEncode("m."+fieldName, field.Type, "\t\t", isEnum)
			g.printf("\t}\n")
			return
		}
		if g.valueOptional(field) {
			// Value style: presence comes from the Has<Field> bool and the
			// value is used directly (no pointer deref).
			g.printf("\tenc.WriteBool(m.Has%s)\n", fieldName)
			g.printf("\tif m.Has%s {\n", fieldName)
			g.generateScalarEncode("m."+fieldName, field.Type, "\t\t", isEnum)
			g.printf("\t}\n")
			return
		}
		// Scalar/string/bytes/enum optionals are *T pointers; deref the value
		// after the presence byte.
		g.printf("\tenc.WriteBool(m.%s != nil)\n", fieldName)
		g.printf("\tif m.%s != nil {\n", fieldName)
		g.generateScalarEncode("*m."+fieldName, field.Type, "\t\t", isEnum)
		g.printf("\t}\n")
		return
	}

	// Handle repeated fields - V2: write count then elements
	if field.Repeated {
		g.printf("\tenc.WriteInt32(int32(len(m.%s)))\n", fieldName)
		g.printf("\tfor _, v := range m.%s {\n", fieldName)
		g.generateScalarEncode("v", field.Type, "\t\t", isEnum)
		g.printf("\t}\n")
		return
	}

	// Handle map fields - V2: write count then key-value pairs
	if field.Type.Kind == ast.TypeMap {
		g.printf("\tenc.WriteInt32(int32(len(m.%s)))\n", fieldName)
		g.printf("\tfor k, v := range m.%s {\n", fieldName)
		g.generateScalarEncode("k", *field.Type.KeyType, "\t\t", false)
		g.generateScalarEncode("v", *field.Type.ValType, "\t\t", g.isEnumType(*field.Type.ValType))
		g.printf("\t}\n")
		return
	}

	// Regular field - V2: no tags
	g.generateScalarEncode("m."+fieldName, field.Type, "\t", isEnum)
}

func (g *Generator) generateScalarEncode(varName string, t ast.FieldType, indent string, isEnum bool) {
	if isEnum || t.Kind == ast.TypeEnum {
		g.printf("%senc.WriteInt32(int32(%s))\n", indent, varName)
		return
	}

	switch t.Kind {
	case ast.TypeBool:
		g.printf("%senc.WriteBool(%s)\n", indent, varName)
	case ast.TypeInt32:
		g.printf("%senc.WriteInt32(%s)\n", indent, varName)
	case ast.TypeInt64:
		g.printf("%senc.WriteInt64(%s)\n", indent, varName)
	case ast.TypeUint32:
		g.printf("%senc.WriteUint32(%s)\n", indent, varName)
	case ast.TypeUint64:
		g.printf("%senc.WriteUint64(%s)\n", indent, varName)
	case ast.TypeFloat32:
		g.printf("%senc.WriteFloat32(%s)\n", indent, varName)
	case ast.TypeFloat64:
		g.printf("%senc.WriteFloat64(%s)\n", indent, varName)
	case ast.TypeString:
		g.printf("%senc.WriteString(%s)\n", indent, varName)
	case ast.TypeBytes:
		g.printf("%senc.WriteBytes(%s)\n", indent, varName)
	case ast.TypeMessage:
		// Nested message fields are represented as Go pointers, so the
		// value can be nil — either an absent optional field or a
		// nil entry inside a repeated/map slice. Calling MarshalTo on
		// a nil pointer would panic; instead, write a 0-length envelope.
		// This is the symmetric counterpart of the decode-side guard
		// that skips unmarshalAt on `len(data) > 0`.
		g.printf("%s{\n", indent)
		g.printf("%s\tnestedEnc := xpb.GetEncoder()\n", indent)
		g.printf("%s\tif %s != nil { %s.MarshalTo(nestedEnc) }\n", indent, varName, varName)
		g.printf("%s\tenc.WriteMessage(nestedEnc.Bytes())\n", indent)
		g.printf("%s\txpb.PutEncoder(nestedEnc)\n", indent)
		g.printf("%s}\n", indent)
	}
}

func (g *Generator) generateFieldDecode(field *ast.Field) {
	fieldName := toCamelCase(field.Name)
	isEnum := g.isEnumType(field.Type)

	// Optional fields: read the 1-byte presence flag first. On false, leave
	// the (nil) pointer as-is and consume nothing more, so the next field
	// decodes correctly. On true, read the value and store its address. The
	// cursor (pos) and err are the function-scoped variables, reassigned with
	// = so the cursor advances across fields.
	if field.Optional {
		g.printf("\t{\n")
		g.printf("\t\tvar present bool\n")
		g.printf("\t\tpresent, pos, err = xpb.ReadBoolAt(data, pos)\n")
		g.printf("\t\tif err != nil { return err }\n")
		g.printf("\t\tif present {\n")
		if field.Type.Kind == ast.TypeMessage && !isEnum {
			// Nested message optional: *T. A present message is always a
			// non-empty envelope (it carries at least its own fields, or a
			// 0-length envelope which we still materialize since presence
			// said so). Read the length-prefixed envelope via the cursor,
			// then recurse with depth+1.
			g.printf("\t\t\tvar mb []byte\n")
			g.printf("\t\t\tmb, pos, err = xpb.ReadMessageBytesAt(data, pos)\n")
			g.printf("\t\t\tif err != nil { return err }\n")
			g.printf("\t\t\tm.%s = &%s{}\n", fieldName, field.Type.Message)
			g.printf("\t\t\tif err := m.%s.unmarshalAt(mb, depth+1); err != nil { return err }\n", fieldName)
		} else if g.valueOptional(field) {
			// Value style: store the decoded value and flip the Has<Field>
			// bool. No pointer is taken, so no per-field boxing allocation.
			if isEnum {
				g.printf("\t\t\tvar v int32\n")
				g.printf("\t\t\tv, pos, err = xpb.ReadInt32At(data, pos)\n")
				g.printf("\t\t\tif err != nil { return err }\n")
				g.printf("\t\t\tm.%s = %s(v)\n", fieldName, field.Type.Message)
			} else {
				g.printf("\t\t\tvar v %s\n", g.goBaseTypeName(field.Type))
				g.printf("\t\t\tv, pos, err = %s\n", g.scalarReadCall(field.Type))
				g.printf("\t\t\tif err != nil { return err }\n")
				g.printf("\t\t\tm.%s = v\n", fieldName)
			}
			g.printf("\t\t\tm.Has%s = true\n", fieldName)
		} else if isEnum {
			g.printf("\t\t\tvar v int32\n")
			g.printf("\t\t\tv, pos, err = xpb.ReadInt32At(data, pos)\n")
			g.printf("\t\t\tif err != nil { return err }\n")
			g.printf("\t\t\tev := %s(v)\n", field.Type.Message)
			g.printf("\t\t\tm.%s = &ev\n", fieldName)
		} else {
			g.printf("\t\t\tvar v %s\n", g.goBaseTypeName(field.Type))
			g.printf("\t\t\tv, pos, err = %s\n", g.scalarReadCall(field.Type))
			g.printf("\t\t\tif err != nil { return err }\n")
			g.printf("\t\t\tm.%s = &v\n", fieldName)
		}
		g.printf("\t\t}\n")
		g.printf("\t}\n")
		return
	}

	// Handle repeated fields - V2: read count then elements
	if field.Repeated {
		elemMin := minWireBytes(field.Type, isEnum)
		g.printf("\t{\n")
		g.printf("\t\tvar count int32\n")
		g.printf("\t\tcount, pos, err = xpb.ReadArrayCountAt(data, pos, %d, %d)\n", elemMin, common.DefaultMaxElements)
		g.printf("\t\tif err != nil { return err }\n")
		g.printf("\t\tm.%s = make(%s, count)\n", fieldName, g.goTypeName(field))
		g.printf("\t\tfor i := int32(0); i < count; i++ {\n")
		if isEnum {
			g.printf("\t\t\tvar v int32\n")
			g.printf("\t\t\tv, pos, err = xpb.ReadInt32At(data, pos)\n")
			g.printf("\t\t\tif err != nil { return err }\n")
			g.printf("\t\t\tm.%s[i] = %s(v)\n", fieldName, field.Type.Message)
		} else {
			g.generateScalarDecodeInto(fmt.Sprintf("m.%s[i]", fieldName), field.Type, "\t\t\t", isEnum)
		}
		g.printf("\t\t}\n")
		g.printf("\t}\n")
		return
	}

	// Handle map fields - V2: read count then key-value pairs
	if field.Type.Kind == ast.TypeMap {
		keyMin := minWireBytes(*field.Type.KeyType, false)
		valMin := minWireBytes(*field.Type.ValType, g.isEnumType(*field.Type.ValType))
		g.printf("\t{\n")
		g.printf("\t\tvar count int32\n")
		g.printf("\t\tcount, pos, err = xpb.ReadArrayCountAt(data, pos, %d, %d)\n", keyMin+valMin, common.DefaultMaxElements)
		g.printf("\t\tif err != nil { return err }\n")
		g.printf("\t\tm.%s = make(%s)\n", fieldName, g.goTypeName(field))
		g.printf("\t\tfor i := int32(0); i < count; i++ {\n")
		g.printf("\t\t\tvar mk %s\n", g.goBaseTypeName(*field.Type.KeyType))
		g.printf("\t\t\tvar mv %s\n", g.goBaseTypeName(*field.Type.ValType))
		// Each decode is wrapped in its own block so the temporary `v`
		// declarations do not collide between key and value reads; pos/err
		// thread through the function-scoped cursor.
		g.printf("\t\t\t{\n")
		g.generateScalarDecodeInto("mk", *field.Type.KeyType, "\t\t\t\t", false)
		g.printf("\t\t\t}\n")
		g.printf("\t\t\t{\n")
		g.generateScalarDecodeInto("mv", *field.Type.ValType, "\t\t\t\t", g.isEnumType(*field.Type.ValType))
		g.printf("\t\t\t}\n")
		g.printf("\t\t\tm.%s[mk] = mv\n", fieldName)
		g.printf("\t\t}\n")
		g.printf("\t}\n")
		return
	}

	// Handle enum field
	if isEnum {
		g.printf("\t{\n")
		g.printf("\t\tvar v int32\n")
		g.printf("\t\tv, pos, err = xpb.ReadInt32At(data, pos)\n")
		g.printf("\t\tif err != nil { return err }\n")
		g.printf("\t\tm.%s = %s(v)\n", fieldName, field.Type.Message)
		g.printf("\t}\n")
		return
	}

	// Regular field - V2: no tags, just read in order
	g.printf("\t{\n")
	g.generateScalarDecodeInto("m."+fieldName, field.Type, "\t\t", isEnum)
	g.printf("\t}\n")
}

// scalarReadCall returns the stateless cursor read expression (which yields
// `(value, newPos, error)` and takes (data, pos)) for a scalar/string/bytes
// type. Used by the optional decode path, which reads into a fresh local
// before taking its address. Not valid for message/map/enum types.
func (g *Generator) scalarReadCall(t ast.FieldType) string {
	switch t.Kind {
	case ast.TypeBool:
		return "xpb.ReadBoolAt(data, pos)"
	case ast.TypeInt32:
		return "xpb.ReadInt32At(data, pos)"
	case ast.TypeInt64:
		return "xpb.ReadInt64At(data, pos)"
	case ast.TypeUint32:
		return "xpb.ReadUint32At(data, pos)"
	case ast.TypeUint64:
		return "xpb.ReadUint64At(data, pos)"
	case ast.TypeFloat32:
		return "xpb.ReadFloat32At(data, pos)"
	case ast.TypeFloat64:
		return "xpb.ReadFloat64At(data, pos)"
	case ast.TypeString:
		return "xpb.ReadStringAt(data, pos)"
	case ast.TypeBytes:
		return g.bytesReadCall()
	}
	return g.bytesReadCall()
}

// generateScalarDecodeInto emits a cursor-threaded read of a scalar/string/
// bytes/enum/message into varName. The cursor (pos) and err are the
// function-scoped variables, advanced with =; the decoded value goes through a
// fresh block-local `v` (each call site is wrapped in its own block, or is a
// fresh loop-body scope, so `v` never collides). The nested-message case reads
// the length-prefixed envelope via the cursor and recurses with depth+1.
func (g *Generator) generateScalarDecodeInto(varName string, t ast.FieldType, indent string, isEnum bool) {
	if isEnum {
		g.printf("%svar v int32\n", indent)
		g.printf("%sv, pos, err = xpb.ReadInt32At(data, pos)\n", indent)
		g.printf("%sif err != nil { return err }\n", indent)
		g.printf("%s%s = %s(v)\n", indent, varName, t.Message)
		return
	}

	if t.Kind == ast.TypeMessage {
		// A nil/absent nested message encoded as a 0-length envelope must
		// decode back to a nil pointer rather than crashing. Skip the
		// allocation + recursive unmarshalAt when the length prefix is
		// empty — calling unmarshalAt on empty bytes errors at the
		// nested type's first ReadString/ReadBytes inside an empty
		// buffer. Leaves the destination nil, which is symmetric with
		// what a caller of the encode side would produce when the field
		// is nil. mb aliases the envelope body via the cursor read.
		g.printf("%svar mb []byte\n", indent)
		g.printf("%smb, pos, err = xpb.ReadMessageBytesAt(data, pos)\n", indent)
		g.printf("%sif err != nil { return err }\n", indent)
		g.printf("%sif len(mb) > 0 {\n", indent)
		g.printf("%s\t%s = &%s{}\n", indent, varName, t.Message)
		g.printf("%s\tif err := %s.unmarshalAt(mb, depth+1); err != nil { return err }\n", indent, varName)
		g.printf("%s}\n", indent)
		return
	}

	g.printf("%svar v %s\n", indent, g.goBaseTypeName(t))
	g.printf("%sv, pos, err = %s\n", indent, g.scalarReadCall(t))
	g.printf("%sif err != nil { return err }\n", indent)
	g.printf("%s%s = v\n", indent, varName)
}

func (g *Generator) goTypeName(field *ast.Field) string {
	t := field.Type
	base := g.goBaseTypeName(t)

	if field.Repeated {
		return "[]" + base
	}
	if t.Kind == ast.TypeMap {
		return fmt.Sprintf("map[%s]%s", g.goBaseTypeName(*t.KeyType), g.goBaseTypeName(*t.ValType))
	}
	// Optional fields are pointers so absence (nil) is distinguishable from a
	// zero value. Message fields are already pointers, so they stay as-is.
	// Under OptionalValue style, scalar/string/bytes/enum optionals are plain
	// values paired with a generated Has<Field> bool instead.
	if field.Optional && t.Kind != ast.TypeMessage && !g.valueOptional(field) {
		return "*" + base
	}
	return base
}

func (g *Generator) goBaseTypeName(t ast.FieldType) string {
	// Check if it's an enum
	if t.Kind == ast.TypeMessage && g.enums[t.Message] {
		return t.Message
	}

	switch t.Kind {
	case ast.TypeBool:
		return "bool"
	case ast.TypeInt32:
		return "int32"
	case ast.TypeInt64:
		return "int64"
	case ast.TypeUint32:
		return "uint32"
	case ast.TypeUint64:
		return "uint64"
	case ast.TypeFloat32:
		return "float32"
	case ast.TypeFloat64:
		return "float64"
	case ast.TypeString:
		return "string"
	case ast.TypeBytes:
		return "[]byte"
	case ast.TypeMessage:
		return "*" + t.Message
	case ast.TypeEnum:
		return t.Enum
	default:
		return "any"
	}
}

func toCamelCase(s string) string {
	parts := strings.Split(s, "_")
	for i := range parts {
		if len(parts[i]) > 0 {
			parts[i] = strings.ToUpper(parts[i][:1]) + parts[i][1:]
		}
	}
	return strings.Join(parts, "")
}

func estimateSize(msg *ast.Message) int {
	size := 0
	for _, field := range msg.Fields {
		switch field.Type.Kind {
		case ast.TypeBool:
			size += wire.SizeBool
		case ast.TypeInt32, ast.TypeUint32, ast.TypeEnum:
			size += wire.SizeInt32
		case ast.TypeInt64, ast.TypeUint64:
			size += wire.SizeInt64
		case ast.TypeFloat32:
			size += wire.SizeFloat32
		case ast.TypeFloat64:
			size += wire.SizeFloat64
		case ast.TypeString, ast.TypeBytes:
			size += 32
		case ast.TypeMessage, ast.TypeMap:
			size += 64
		}
		if field.Repeated {
			size *= 4
		}
	}
	if size < 64 {
		return 64
	}
	return size
}

// minWireBytes returns the smallest possible on-wire size for one element of
// the given type. Used to bound the count of a repeated/map field against the
// remaining buffer in ReadArrayCount.
func minWireBytes(t ast.FieldType, isEnum bool) int {
	if isEnum {
		return 4
	}
	switch t.Kind {
	case ast.TypeBool:
		return 1
	case ast.TypeInt32, ast.TypeUint32, ast.TypeFloat32:
		return 4
	case ast.TypeInt64, ast.TypeUint64, ast.TypeFloat64:
		return 8
	case ast.TypeString, ast.TypeBytes, ast.TypeMessage:
		return 1
	}
	return 1
}
